class Car {
    public String name;
    private String brand;
    private int speed;
    private String color;

    void setter(String brand, int speed, String color){
        this.brand = brand;
        if(speed < 0){
            System.out.println("Speed cannot be negative");
        }
        else{
            this.speed = speed;
        }
        this.color = color;
    }
    String xyz(){
        return brand;
    }
    int getterSpeed(){
        return speed;
    }
    String getColor(){
        return color;
    }
}



public class Main {
    public static void main(String[] args) {
        Car myCar = new Car();

        myCar.name = "bmw";
        myCar.setter("X5", 200,"red");
        System.out.println(myCar.name);
        System.out.println(myCar.xyz());
        System.out.println(myCar.getterSpeed());
        System.out.println(myCar.getColor());
    }
}
